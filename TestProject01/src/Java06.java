
public class Java06 {

	public static void main(String[] args) {
		int a = 10;
		System.out.println(a++);
		System.out.println(a++);
		System.out.println(a++);
		System.out.println(a++);
		System.out.println();
		
		int b = 10;
		System.out.println(++b);
		System.out.println(++b);
		System.out.println(++b);
		System.out.println(++b);
		System.out.println();
		
		int c = 10;
		System.out.println(c--);
		System.out.println(c--);
		System.out.println(c--);
		System.out.println(c--);
		System.out.println();
		
		int d = 10;
		System.out.println(--d);
		System.out.println(--d);
		System.out.println(--d);
		System.out.println(--d);
		System.out.println();
		
		
	}

}
